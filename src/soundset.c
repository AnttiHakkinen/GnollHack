struct obj* src, * dest;
{
    sound_source* ss;

    for (ss = sound_base; ss; ss = ss->next)
    {
        if (ss->type == SOUNDSOURCE_OBJECT && ss->id.a_obj == src)
            ss->id.a_obj = dest;
    }

    src->makingsound = 0;
    dest->makingsound = 1;
}

